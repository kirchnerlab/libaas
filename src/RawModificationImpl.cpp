RawModificationImpl::RawModificationImpl(RawModificationImplKeyType id) :
		id_(id) {
	libaas::Size i = 0;
	libaas::Size k = 0;
	for (i = 0; i < nModifications; ++i) {
		if (id == titles[i]) {
			break;
		}
		k += nDeltas[i];
	}
	if (i == nModifications) {
		throw std::out_of_range(
				"RawModificationImpl::RawModificationImpl(): Cannot find modification.");
	}

	// TODO hier fehlt noch einiges + member passen nicht

	psiName_ = titles[i];
	StoichiometryConfig sc(StoichiometryConfigImpl::DEFAULT_ELEMENT_CONFIG);
	typedef StoichiometryConfigImpl::const_iterator CIT;
	libaas::Size end = k+nDeltas[i];
	for (; k < end; ++k) {
		CIT tmp = sc.get().find(deltaSymbols[k]);
		if (tmp != sc.get().end()) {
			stoichiometry_.set(libaas::elements::Element(tmp->second), deltaNumbers[k]);
		} else {
			throw std::out_of_range(
					"RawModificationImpl::RawModificationImpl(): Cannot find element symbol.");
		}
	}
}

